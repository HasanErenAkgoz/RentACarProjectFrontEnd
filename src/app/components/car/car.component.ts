import { Component, OnInit } from '@angular/core';
import { ActivatedRoute } from '@angular/router';
import { Brand } from 'src/app/models/brand';
import { CarDetail } from 'src/app/models/carDetail';
import { BrandService } from 'src/app/services/brand.service';
import { CarService } from 'src/app/services/car.service';

@Component({
  selector: 'app-car',
  templateUrl: './car.component.html',
  styleUrls: ['./car.component.css'],
})
export class CarComponent implements OnInit {
  cars: CarDetail[] = [];
  brandList: Brand[] = [];
  filterText:string="";
  empty:boolean=false;
  brandFilter:number;
  currentBrandId:number;
  dataLoaded: boolean = false;
  defaultPath: string = 'https://localhost:44323/images/';
  defaultImage: string = 'default.jpg';
  constructor(
    private carService: CarService,
    private brandService: BrandService,
    private activatedRout:ActivatedRoute
  ) {}
  ngOnInit(): void {
    this.activatedRout.params.subscribe(params=>{
      if(params["brandId"])
      this.getCarListBrandId(params["brandId"]);
    })
    this.getCars();
    this.getBrand();
  }
  getCars() {
    this.carService.getCars().subscribe((response) => {
      this.cars = response.data;
      this.dataLoaded = true;
      this.empty=false;
    });
  }
  getBrand(){
    this.brandService.getBrands().subscribe(response=>{
      this.brandList=response.data;
    })
  }
  setCurrentBrand(brandId:number)
  {
    return(brandId===this.currentBrandId?true:false)
  }

getCarListBrandId(brandId:number){
  this.carService.getCarListBrandId(brandId).subscribe(response=>{
    this.cars=response.data;
    if(this.cars.length==0)
    {
this.empty=true
    }
    else
    this.empty=false;

  })
}

}
